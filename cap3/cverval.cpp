// cverval.cpp
// visualiza diferentes tipos de valores con <<
#include <iostream>
using namespace std;

int main()
{
    char c = 'C';
    char carac[] = "Un mensaje";
    int i = 46;
    long int m = 91234;
    float x = 34.719;
    double d = 3.1415192;

    cout << "c es: " << c << "\n";
    cout << "carac[] es: " << carac << "\n";
    cout << "i es: " << i << "\n";
    cout << "m es: " << m << "\n";
    cout << "x es: " << x << "\n";
    cout << "d es: " << d << "\n";
}